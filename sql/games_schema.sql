-- config tables: อ่านได้ทุกคนที่ login, เขียนได้เฉพาะ admin
do $$
declare
  t text;
begin
  foreach t in array array['exp_rewards','game_stages','game_backgrounds','game_enemies','game_music','game_sfx','shop_items']
  loop
    execute format('drop policy if exists %I_read on public.%I', t, t);
    execute format('create policy %I_read on public.%I for select to authenticated using (true)', t, t);
    execute format('drop policy if exists %I_admin_write on public.%I', t, t);
    execute format('create policy %I_admin_write on public.%I for all to authenticated using (public.is_admin()) with check (public.is_admin())', t, t);
  end loop;
end $$;

