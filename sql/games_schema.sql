-- ---------------------------------------------------------------------
-- 10. user_upgrades : อัปเกรดถาวรที่ผู้ใช้ซื้อแล้ว
-- ---------------------------------------------------------------------
create table if not exists public.user_upgrades (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  item_id bigint not null references public.shop_items(id) on delete cascade,
  created_at timestamptz not null default now(),
  unique (user_id, item_id)
);

-- ---------------------------------------------------------------------
-- 11. user_items : ไอเทมใช้แล้วหมด (inventory)
-- ---------------------------------------------------------------------
create table if not exists public.user_items (
  user_id uuid not null references auth.users(id) on delete cascade,
  item_id bigint not null references public.shop_items(id) on delete cascade,
  quantity integer not null default 0,
  primary key (user_id, item_id)
);

