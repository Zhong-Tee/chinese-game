-- =====================================================================
-- RPC: เพิ่ม EXP / Coin แบบ atomic (กันค่าหายเมื่อแก้พร้อมกัน)
-- =====================================================================
create or replace function public.add_game_currency(p_exp integer, p_coin integer)
returns public.user_game_state
language plpgsql
security definer
as $$
declare
  result public.user_game_state;
begin
  insert into public.user_game_state (user_id, exp, coin, updated_at)
  values (auth.uid(), greatest(p_exp, 0), greatest(p_coin, 0), now())
  on conflict (user_id) do update
    set exp = public.user_game_state.exp + greatest(p_exp, 0),
        coin = public.user_game_state.coin + greatest(p_coin, 0),
        updated_at = now()
  returning * into result;
  return result;
end;
$$;

-- RPC: หักเงินเพื่อซื้อของ (คืน true ถ้าสำเร็จ)
create or replace function public.spend_game_currency(p_currency text, p_amount integer)
returns boolean
language plpgsql
security definer
as $$
declare
  ok boolean := false;
begin
  if p_currency = 'exp' then
    update public.user_game_state
      set exp = exp - p_amount, updated_at = now()
      where user_id = auth.uid() and exp >= p_amount;
    ok := found;
  elsif p_currency = 'coin' then
    update public.user_game_state
      set coin = coin - p_amount, updated_at = now()
      where user_id = auth.uid() and coin >= p_amount;
    ok := found;
  end if;
  return ok;
end;
$$;

