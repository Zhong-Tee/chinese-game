values ('game-assets', 'game-assets', true)
on conflict (id) do nothing;

drop policy if exists "game-assets public read" on storage.objects;
create policy "game-assets public read" on storage.objects
  for select to public using (bucket_id = 'game-assets');

drop policy if exists "game-assets admin write" on storage.objects;
create policy "game-assets admin write" on storage.objects
  for all to authenticated
  using (bucket_id = 'game-assets' and public.is_admin())
  with check (bucket_id = 'game-assets' and public.is_admin());

