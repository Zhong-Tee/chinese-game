-- ปลอดภัยที่จะรันซ้ำ (idempotent)
--   1) ตัวละครที่ผู้เล่นเลือกได้ (admin อัปโหลดรูป)
--   2) รูปด่าน (map icon) บนหน้าเลือกด่าน
--   3) เวลาตอบแยกระหว่างโจทย์แบบเลือกตอบ กับ โจทย์เรียงประโยค
